CREATE TABLE place (
	place_id INT NOT NULL PRIMARY KEY,
	country VARCHAR(max) NOT NULL,
	region VARCHAR(max) NOT NULL,
	city VARCHAR(max) NOT NULL,
	district VARCHAR(max) NOT NULL
);

